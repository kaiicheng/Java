import java.util.ArrayList;

public class SizeOfAnArrayList {

	public static void main(String[] args) {

		ArrayList<Integer> nums = new ArrayList<Integer>();
		nums.add(1);
		nums.add(3);
		nums.add(5);
		nums.add(7);
	    System.out.println(nums);
	    
	    System.out.println(nums.size());
	}
}
